# ── 从经典回忆录中提炼的写作技法 ─────────────────────────────

# 以下技法提炼自杨绛《我们仨》、陆庆屹《四个春天》、齐邦媛《巨流河》
# 这些不是抽象规则，而是可以直接学习的具体手法

WRITING_TECHNIQUES = """
## 写作技法（提炼自经典中文回忆录）

### 1. 用细节代替形容词
不要说"那时候很穷"、"父亲很慈祥"。要写：
- 杨绛写丈夫："他笑得都站不直了。我隔着他的肚皮，也能看到他肚子里翻滚的笑浪。"
- 陆庆屹写父亲："爸一只手揣在手套里，脚焐在装有热水袋的脚套里，木铲子在锅里一圈一圈地划，手冷了就换另一只，满头白发在阴冷的空气里微微颤动。"
→ 一个动作、一个画面，胜过一百个形容词。把情感藏在具体的细节里。

### 2. 日常即诗
最打动人的往往不是大事件，而是日常的细碎时刻：
- 停电的除夕夜，一家人去山里散步
- 妈妈用普通话说"谢谢你的情啊，谢谢你的爱呀"
- 三个人的目光同时聚焦在一把木铲上
→ 平凡时刻里藏着最深的爱。写回忆录时，多回忆这些小事。

### 3. 对话让人物活起来
让人物自己说话，比作者描述更生动：
- 杨绛写女儿："Mummy娘！爸爸做坏事！当场拿获！"
- 陆庆屹写妈妈："谢谢你的情啊，谢谢你的爱呀。"爸爸说："谢什么鬼啊。"
→ 真实的对话里有人物的性格、关系和时代气息。

### 4. 情感要有"容器"
不要直接说"我很悲伤""我很幸福"。让场景、物品、动作成为情感的容器：
- 杨绛用一个"万里长梦"来装失去亲人的痛
- 陆庆屹用"一扇窗框住的父亲"来装对家的眷恋
→ 找到那个具体的"容器"，情感就有了重量。

### 5. 节奏感很重要
- 短句像呼吸，长句像流水
- 紧张的段落用短句，回忆的段落用长句
- 段落之间留白，让读者喘口气
- 杨绛的段落极短，有时一句话就是一段，像诗一样

### 6. 以"我"的眼睛看世界
回忆录是"我看见、我记得、我感受到"，不要写成客观历史：
- 齐邦媛从巨流河写到哑口海，但始终是她自己的眼睛在看
- 陆庆屹拍纪录片的心态："我觉得每一次都有特别的意义"
→ 这不是历史书，是一个人的视角。保持这种主观的真诚。

### 7. 开头要有钩子
- 杨绛以一场梦开头，立即制造悬念
- 齐邦媛以一条河的地理/历史意义开头，铺开宏大格局
- 陆庆屹从一个具体画面（窗框里的父亲）开头，温暖而安静
→ 开头决定读者要不要继续读下去。用一个具体的画面、一句有分量的话、一个让人好奇的场景来开篇。
"""

